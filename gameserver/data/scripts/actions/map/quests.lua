	storageValue = 203,
	item = {id = 1990, count = 1}, -- a present
	content = {
		{id = 2326, count = 1}, -- an annihilation bear
	},
}

local action = Action()

function action.onUse(player, item, fromPosition, target, toPosition)
	if item:getActionId() == 1203 then -- Annihilator rewards
		if fromPosition.x == 33227 and fromPosition.y == 31656 and fromPosition.z == 13 then
			return onUseQuest(player, item, demonArmorQuest)
		elseif fromPosition.x == 33229 and fromPosition.y == 31656 and fromPosition.z == 13 then
			return onUseQuest(player, item, magicSwordQuest)
		elseif fromPosition.x == 33231 and fromPosition.y == 31656 and fromPosition.z == 13 then
			return onUseQuest(player, item, stonecutterAxeQuest)
		elseif fromPosition.x == 33233 and fromPosition.y == 31656 and fromPosition.z == 13 then
			return onUseQuest(player, item, annihilationBearQuest)
		end
		return true
	end
	
	local chest = chests[item:getActionId()]
	if not chest then
		return false
	end
	
	return onUseQuest(player, item, chest)
end

for i = 1000, 2000, 1 do
	action:aid(i)
end
action:register()
